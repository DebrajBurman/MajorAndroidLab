package com.example.majorproject.HomeAdapter;

public class CategoriesHelperClass {
    int image;
    String category;

    public CategoriesHelperClass(int image, String category) {
        this.image = image;
        this.category = category;
    }

    public int getImage() {
        return image;
    }

    public String getCategory() {
        return category;
    }
}
